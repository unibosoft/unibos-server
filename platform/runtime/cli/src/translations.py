def detect_system_language():
    """Detect system language and return appropriate language code"""
    try:
        system_lang = locale.getdefaultlocale()[0]
        if system_lang:
            lang_code = system_lang.split('_')[0].lower()
            if lang_code in TRANSLATIONS:
                return lang_code
    except:
        pass
    return 'en'  # Default to English

def get_translation(key, lang_code):
    """Get translation for a specific key"""
    if lang_code in TRANSLATIONS and key in TRANSLATIONS[lang_code]:
        return TRANSLATIONS[lang_code][key]
    # Fallback to English
    if key in TRANSLATIONS['en']:
        return TRANSLATIONS['en'][key]
    return key  # Return key itself if not found

def get_available_languages():
    """Get list of available languages"""
    languages = []
    for code, data in TRANSLATIONS.items():
        languages.append((code, data['name'], data['flag']))
    return languages